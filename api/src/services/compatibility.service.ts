export async function createComp(input: CreateCompInput, userId?: string): Promise<CompatibilityGroup> {
  const [row] = await db('compatibility_groups')
    .insert({
      id: input.id,
      name: input.name,
      description: input.description,
      auto_generated: false,
      created_by: userId,
    })
    .returning('*');

  // Add spa memberships if provided
  if (input.spaModelIds && input.spaModelIds.length > 0) {
    const compSpas = input.spaModelIds.map((spaId) => ({
      comp_id: row.id,
      spa_model_id: spaId,
    }));
    await db('comp_spas').insert(compSpas);
  }

  await logAudit('compatibility_groups', row.id, 'INSERT', null, row, userId);

  return (await getCompById(row.id))!;
}

export async function updateComp(
  id: string,
  input: UpdateCompInput,
  userId?: string
): Promise<CompatibilityGroup | null> {
  const oldRow = await db('compatibility_groups').where({ id }).first();
  if (!oldRow || oldRow.deleted_at) return null;

  const updateData: Record<string, unknown> = { updated_at: db.fn.now() };
  if (input.name !== undefined) updateData.name = input.name;
  if (input.description !== undefined) updateData.description = input.description;

  const [row] = await db('compatibility_groups').where({ id }).update(updateData).returning('*');

  await logAudit('compatibility_groups', row.id, 'UPDATE', oldRow, row, userId);
  return getCompById(row.id);
}

export async function deleteComp(id: string, userId?: string): Promise<boolean> {
  const oldRow = await db('compatibility_groups').where({ id }).first();
  if (!oldRow || oldRow.deleted_at) return false;

  await db('compatibility_groups').where({ id }).update({ deleted_at: db.fn.now() });
  await logAudit('compatibility_groups', id, 'DELETE', oldRow, null, userId);
  return true;
}

