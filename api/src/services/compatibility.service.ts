export async function generateCompIdSuggestion(
  brandAbbrev: string,
  categoryAbbrev: string
): Promise<string> {
  // Find highest existing number for this pattern
  const pattern = `COMP-${brandAbbrev.toUpperCase()}-${categoryAbbrev.toUpperCase()}-%`;
  const existing = await db('compatibility_groups')
    .select('id')
    .whereRaw('id LIKE ?', [pattern])
    .orderBy('id', 'desc')
    .limit(1);

  if (existing.length === 0) {
    return `COMP-${brandAbbrev.toUpperCase()}-${categoryAbbrev.toUpperCase()}-001`;
  }

  const lastId = existing[0].id;
  const numPart = lastId.split('-').pop();
  const nextNum = parseInt(numPart!, 10) + 1;
  return `COMP-${brandAbbrev.toUpperCase()}-${categoryAbbrev.toUpperCase()}-${String(nextNum).padStart(3, '0')}`;
}

export async function shouldAutoGenerateComp(
  partId: string,
  spaModelIds: string[],
  categoryId: string
): Promise<{ shouldGenerate: boolean; existingComp?: CompatibilityGroup }> {
  if (spaModelIds.length < 2) {
    return { shouldGenerate: false };
  }

  // Check if there's an existing Comp with exact same spas in same category
  const comps = await db('compatibility_groups')
    .select('compatibility_groups.*')
    .where('source_category_id', categoryId)
    .whereNull('deleted_at');

  for (const comp of comps) {
    const compSpas = await db('comp_spas').select('spa_model_id').where('comp_id', comp.id);
    const compSpaIds = compSpas.map((cs) => cs.spa_model_id).sort();

    if (
      compSpaIds.length === spaModelIds.length &&
      compSpaIds.every((id, i) => id === [...spaModelIds].sort()[i])
    ) {
      return { shouldGenerate: false, existingComp: mapCompFromDb(comp) };
    }
  }

  // Check if 2+ parts share these exact spas in this category
  const partsWithSameCompat = await db('part_spa_compatibility')
    .select('part_id')
    .leftJoin('pcdb_parts', 'part_spa_compatibility.part_id', 'pcdb_parts.id')
    .whereIn('spa_model_id', spaModelIds)
    .where('pcdb_parts.category_id', categoryId)
    .where('status', 'confirmed')
    .groupBy('part_id')
    .havingRaw('COUNT(DISTINCT spa_model_id) = ?', [spaModelIds.length]);

  return { shouldGenerate: partsWithSameCompat.length >= 2 };
}

