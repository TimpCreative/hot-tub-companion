// Near-Match Suggestions
// =============================================================================

export async function findNearMatchComps(
  spaModelIds: string[],
  categoryId?: string,
  threshold = 0.5
): Promise<CompNearMatch[]> {
  if (spaModelIds.length === 0) return [];

  // Get all Comps
  let compsQuery = db('compatibility_groups')
    .select('compatibility_groups.*')
    .whereNull('compatibility_groups.deleted_at');

  if (categoryId) {
    compsQuery = compsQuery.where('compatibility_groups.source_category_id', categoryId);
  }

  const comps = await compsQuery;
  const results: CompNearMatch[] = [];

  for (const comp of comps) {
    const compSpas = await db('comp_spas').select('spa_model_id').where('comp_id', comp.id);
    const compSpaIds = new Set(compSpas.map((cs) => cs.spa_model_id));

    if (compSpaIds.size === 0) continue;

    // Count matching spas
    const matchingCount = spaModelIds.filter((id) => compSpaIds.has(id)).length;
    const matchPercentage = matchingCount / compSpaIds.size;

    if (matchPercentage >= threshold) {
      results.push({
        comp: mapCompFromDb({ ...comp, spa_count: String(compSpaIds.size) }),
        matchingSpas: matchingCount,
        totalCompSpas: compSpaIds.size,
        matchPercentage,
      });
    }
  }

  // Sort by match percentage descending
  results.sort((a, b) => b.matchPercentage - a.matchPercentage);

  return results;
}

