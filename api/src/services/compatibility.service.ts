export async function listComps(
  includeDeleted = false,
  pagination?: PaginationParams
): Promise<{ comps: CompatibilityGroup[]; total: number }> {
  let query = db('compatibility_groups')
    .select(
      'compatibility_groups.*',
      db.raw('(SELECT COUNT(*) FROM comp_spas WHERE comp_spas.comp_id = compatibility_groups.id) as spa_count'),
      'pcdb_categories.name as source_category_name'
    )
    .leftJoin('pcdb_categories', 'compatibility_groups.source_category_id', 'pcdb_categories.id');

  if (!includeDeleted) {
    query = query.whereNull('compatibility_groups.deleted_at');
  }

  const countQuery = includeDeleted
    ? db('compatibility_groups')
    : db('compatibility_groups').whereNull('deleted_at');
  const countResult = await countQuery.count('* as count').first();
  const total = parseInt((countResult?.count as string) ?? '0', 10);

  const sortBy = pagination?.sortBy || 'compatibility_groups.id';
  const sortOrder = pagination?.sortOrder || 'asc';
  query = query.orderBy(sortBy, sortOrder);

  if (pagination?.page && pagination?.pageSize) {
    const offset = (pagination.page - 1) * pagination.pageSize;
    query = query.offset(offset).limit(pagination.pageSize);
  }

  const rows = await query;
  return {
    comps: rows.map(mapCompFromDb),
    total,
  };
}

