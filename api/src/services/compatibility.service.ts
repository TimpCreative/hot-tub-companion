function mapCompatibilityFromDb(
  row: DbPartSpaCompatibility & {
    part_name?: string;
    part_number?: string;
    spa_model_name?: string;
    spa_year?: number;
    brand_name?: string;
  }
): PartSpaCompatibility {
  return {
    partId: row.part_id,
    spaModelId: row.spa_model_id,
    status: row.status,
    fitNotes: row.fit_notes,
    quantityRequired: row.quantity_required,
    position: row.position,
    source: row.source,
    addedBy: row.added_by,
    addedAt: row.added_at,
    reviewedBy: row.reviewed_by,
    reviewedAt: row.reviewed_at,
    partName: row.part_name,
    partNumber: row.part_number,
    spaModelName: row.spa_model_name,
    spaYear: row.spa_year,
    brandName: row.brand_name,
  };
}

function mapCompFromDb(
  row: DbCompatibilityGroup & {
    spa_count?: string;
    part_count?: string;
    source_category_name?: string;
  }
): CompatibilityGroup {
  return {
    id: row.id,
    name: row.name,
    description: row.description,
    autoGenerated: row.auto_generated,
    sourceCategoryId: row.source_category_id,
    createdBy: row.created_by,
    deletedAt: row.deleted_at,
    createdAt: row.created_at,
    updatedAt: row.updated_at,
    spaCount: row.spa_count ? parseInt(row.spa_count, 10) : undefined,
    partCount: row.part_count ? parseInt(row.part_count, 10) : undefined,
    sourceCategoryName: row.source_category_name,
  };
}

