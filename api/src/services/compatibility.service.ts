  if (pagination?.page && pagination?.pageSize) {
    const offset = (pagination.page - 1) * pagination.pageSize;
    query = query.offset(offset).limit(pagination.pageSize);
  }

  const rows = await query;
  return {
    items: rows.map(mapCompatibilityFromDb),
    total,
  };
