export async function getPartsForComp(compId: string): Promise<string[]> {
  // Parts that are compatible with ALL spas in the Comp
  const compSpas = await db('comp_spas').select('spa_model_id').where('comp_id', compId);
  if (compSpas.length === 0) return [];

  const spaIds = compSpas.map((cs) => cs.spa_model_id);

  // Find parts where the count of confirmed compatibilities equals the number of spas in the Comp
  const rows = await db('part_spa_compatibility')
    .select('part_id')
    .whereIn('spa_model_id', spaIds)
    .where('status', 'confirmed')
    .groupBy('part_id')
    .havingRaw('COUNT(DISTINCT spa_model_id) = ?', [spaIds.length]);

  return rows.map((r) => r.part_id);
}

